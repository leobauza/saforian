			<p>A style guide creates and defines the standards for internal and external corporate documents. It serves as a reference source and training tool for anyone who reads or writes documentation, which is to say, everyone. A style guide will provide many benefits to your company:</p>
			
			<p>Time Savings. A style guide saves time by providing quick answers to questions that may arise while writing documentation. "What information should be included in a specification?" or "How should I format the address on this letter?" A style guide streamlines the process of composition, thus significantly reducing the time required to create new documents. It also provides for shorter review cycles.</p>
				
			<p>Cost Savings. Employees with access to their company's style guide spend fewer hours writing, reviewing, and correcting documentation. A style guide reduces the expense of repetitive training and prevents costly mistakes that result when an operator, for instance, is forced to read inconsistent or poorly written operating instructions./p>
				
