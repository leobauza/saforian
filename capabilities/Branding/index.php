			<h3>Naming:</h3>
			
			<p>Great names are a powerful force for the companies they work flikr. They differentiate you from competitors, make an emotional connection with your audience, and help to build a brand that evokes the passions of your customers.</p>
			
			<p>A name can:
				<ul>
					<li>Separate you from your competitors</li>
					<li>Show the world that you are different</li>
					<li>Reinforce a unique positioning platform</li>
					<li>Create positive and lasting engagement with your audience</li>
					<li>Make you unforgettable</li>
					<li>Become no-cost, self sustaining PR vehicle</li>
					<li>Provide a deep well of marketing and advertising images</li>
					<li>Be the genesis of a brand that rises above the goods and services you provide</li>
					<li>Completely dominate a category</li>
				</ul>
			</p>
			
			<p>There are typically four categories in which product or companynames fall into:
				<ul>
					<li>Functional</li>
					<li>Invented</li>
					<li>Experiential</li>
					<li>Evocative</li>
				</ul>
			</p>
			
			<p>All four categories have positives (+) and negatives (-).</p>
			
			<p><b>Functional Names:</b></p>
			<p>Examples: Subway, Martha Stewart, U.S. Airways</p>
			
			<p>Direct bulk of the brand equity is related to the company name</p>
				<ul>
					<li>The name only performs one task</li>
					<li>Because these names are common in their categories, they become indistinguishable from the majority of their competitors</li>
					<li>They are hard to trademark</li>
					<li>They are not unique</li>
				</ul>
			</p>
			
			<p><b>Invented Names:</b></p>
			<p>There are generally two types of invented names:</p>
			
			<p>A) Names derived from Greek or Latin roots<br/>Examples: Acquient, Agilent, Alliant</p>
				<ul>
					<li>Free from negative connotations</li>
					<li>Serious sounding</li>
					<li>Hard for consumers to attach meaning to</li>
					<li>Difficult to remember or pronounce</li>
					<li>Can sound cold and sanitized</li>
					<li>Image-free and emotionally void (little potential marketing energy)</li>
				</ul>
			</p>
					
			<p>A) Poetically constructed names based on rhythm and the experience of saying them<br/>
			Examples: Snapple, Google, Oreo</p>
				<ul>
					<li>Target audience likes saying these fun names</li>
					<li>Highly memorable</li>
					<li>Emotionally engaging</li>
					<li>Rich with marketing energy</li>
					<li>Typically difficult to get corporate approval for</li>
				</ul>
			</p>
			
			<p><b>Experiential Names</b></p>
			<p>Examples: Infoseek, Explorer, Safari</p>
				<ul>
					<li>Make sense to the consumer</li>
					<li>Relate the experience to the product</li>
					<li>Most effective for early entrants into a business sector</li>
					<li>Common / high frequency in many sectors</li>
					<li>Over-usage makes them less effective (For example Explorer and Safari are also car names. These names say the same thing in the same way to the same audience.)</li>
				</ul>
			</p>
			
			<p><b>Evocative Names</b></p>
			<p>Examples: Yahoo, Apple, Crest</p>
				<ul>
					<li>Evokes the positioning of a product, rather than describing a function or direct experience</li>
					<li>Rare and different</li>
					<li>Multidimensional, making the name highly engaging</li>
					<li>Can become a branding force that dominates its industry</li>
					<li>When created out of sync with brand positioning, it’s not effective</li>
					<li>Typically difficult to get corporate approval for</li>
				</ul>
			</p>
			
			<p><b>Remember:</b></p>
				<ul>
					<li>Discovering good names is virtually never love at first sight</li>
					<li>A name that looks and sounds a little different could have the unique edge that makes it memorable</li>
					<li>A name that promotes an idea that’s different from everything else in the market, could be the name consumers remember in the end</li>
					<li>Be willing to consider something different than what you have in mind right now</li>
				</ul>
			</p>
			
			<p>Structured exploration is key to the effective development of a name. Whether functional, invented, experiential or evocative, the right name captures the essence of the cause and the organization at hand. Many good ideas can emerge, but it's essential to focus ideas so the final name isn't just good, it's right.</p>
			
