				<h2>Integration with Scheduling System</h2>
				<p>We developed a hybrid solution that allowed US Inspect to continue using their custom scheduling system while we created a dynamic front-end engine with linked user records. To connect users with the backend scheduling system, we came up with a custom solution that used the standard Drupal login system interfaced to the authorization backend from US Inspect. This allowed us to log in existing users and create new ones on the fly using their system as a user source while creating new Drupal users to match. Our solution provides personalization information via Drupal, synchronization of credentials with US Inspect services, and the ability to launch the scheduler in a new window using authorized linked accounts.</p>
			</article>
			<article class="span4">
				<figure class="center-img">
					<img src="../../images/caseStudies/global_interactive_storytelling_logo.png" alt="Browncoat">
				</figure>
				<h2>Browncoat</h2>
				<p>To streamline web development workflow, we used our base frontend framework Browncoat. This increased the speed of development while maintaining code quality, consistency, and structure. Browncoat has a variety of elements built in to the framework to reduce development time like styles, color schemes, and grid structure. Responsive design elements are also built in to Browncoat to create an experience that is accessible across a variety of platforms.</p>
			</article>
			<article class="span4 l">
				<figure class="center-img">
					<img src="../../images/caseStudies/global_responsive_logo.png" alt="Responsive">
				</figure>
				<h2>Responsive Design</h2>
				<p>To accommodate US Inspect’s diverse audience, we built the site using responsive design to adapt to different devices. Whether you access the site on a tablet, phone, laptop, desktop computer, or TV, we ensured that the content works the same across all devices and platforms. To reduce development time, we incorporated responsive design elements into our Browncoat Framework to enable mobile access and content consumption.</p>
